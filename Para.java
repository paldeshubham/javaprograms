   // java program of parametrized constructor
   class Para{
   	// creating parameterized constructor
   	Para(int a,int b)
   	{
   		System.out.println("Addition is:"+(a+b));
   	}
   	public static void main(String args[])
   	{
   		//calling parameterized constructor
   		Para p1= new Para(6,7);
   	}
   }
